import eu.verdelhan.acr122urw.HexUtils;
import org.nfctools.mf.MfException;
import org.nfctools.mf.block.BlockResolver;
import org.nfctools.mf.block.MfBlock;
import org.nfctools.mf.classic.*;

import javax.smartcardio.CardException;
import java.io.IOException;
import java.util.Arrays;
import java.util.List;

import static eu.verdelhan.acr122urw.HexUtils.bytesToHexString;
import static eu.verdelhan.acr122urw.HexUtils.isHexString;

/**
 * Mifare utility class.
 */
public final class ReadWriteUtils {

    /** Mifare Classic 1K sector count */
    public static final int MIFARE_1K_SECTOR_COUNT = 16;

    /** Mifare Classic 1K block count (per sector) */
    public static final int MIFARE_1K_PER_SECTOR_BLOCK_COUNT = 4;

    /** Common Mifare Classic 1K keys */
    public static final List<String> COMMON_MIFARE_CLASSIC_1K_KEYS = Arrays.asList(
            "001122334455",
            "000102030405",
            "A0A1A2A3A4A5",
            "B0B1B2B3B4B5",
            "AAAAAAAAAAAA",
            "BBBBBBBBBBBB",
            "AABBCCDDEEFF",
            "FFFFFFFFFFFF"
    );

    private ReadWriteUtils() {
    }

    /**
     * @param s a string
     * @return true if the provided string is a valid Mifare Classic 1K key, false otherwise
     */
    public static boolean isValidMifareClassic1KKey(byte[] s) {
        return isHexString(bytesToHexString(s)) && (s.length == 6);
    }

    /**
     * @param s a string
     * @return true if the provided string is a valid Mifare Classic 1K sector index, false otherwise
     */
    public static boolean isValidMifareClassic1KSectorIndex(String s) {
        try {
            int sectorIndex = Integer.parseInt(s);
            return sectorIndex >= 0 && sectorIndex < MIFARE_1K_SECTOR_COUNT;
        } catch (NumberFormatException nfe) {
            return false;
        }
    }

    /**
     * @param s a string
     * @return true if the provided string is a valid Mifare Classic 1K block index, false otherwise
     */
    public static boolean isValidMifareClassic1KBlockIndex(String s) {
        try {
            int sectorIndex = Integer.parseInt(s);
            return sectorIndex >= 0 && sectorIndex < MIFARE_1K_PER_SECTOR_BLOCK_COUNT;
        } catch (NumberFormatException nfe) {
            return false;
        }
    }

    /**
     * Write data to a Mifare Classic 1K card.
     * @param readerWriter the reader
     * @param sectorId the sector to be written
     * @param blockId the block to be written
     * @param key the key to be used for writing
     * @param data the data
     */
    public static boolean writeToMifareClassic1KCard(MfClassicReaderWriter readerWriter, int sectorId, int blockId, byte[] key, byte[] data)
            throws CardException {
        if (!isValidMifareClassic1KKey(key)) {
            System.out.println("The key " + key + "is not valid.");
            return false;
        }

        // Reading with key A
        MemoryLayout memoryLayout = readerWriter.getMemoryLayout();
        MfClassicAccess access = new MfClassicAccess(new KeyValue(Key.A, key), sectorId, blockId,
                memoryLayout.getBlocksPerSector(sectorId));

        String blockData = readMifareClassic1KBlock(readerWriter, access);
        if (blockData == null) {
            // Reading with key B
            access = new MfClassicAccess(new KeyValue(Key.B, key), sectorId, blockId,
                    memoryLayout.getBlocksPerSector(sectorId));
            blockData = readMifareClassic1KBlock(readerWriter, access);
        }
        System.out.print("Old block data: ");
        if (blockData == null) {
            // Failed to read block
            System.out.println("<Failed to read block>");
        } else {
            // Block read
            System.out.println(blockData + " (Key " + access.getKeyValue().getKey().name() + ": " + HexUtils.bytesToHexString(key) + ")");

            // Writing with same key
            boolean written = false;
            try {
                MfBlock block = BlockResolver.resolveBlock(MemoryLayout.CLASSIC_1K, sectorId, blockId, data);
                written = writeMifareClassic1KBlock(readerWriter, access, block);
            } catch (MfException me) {
                System.out.println(me.getMessage());
            }
            if (written) {
                blockData = readMifareClassic1KBlock(readerWriter, access);
                System.out.print("New block data: ");
                if (blockData == null) {
                    // Failed to read block
                    System.out.println("<Failed to read block>");
                } else {
                    // Block read
                    System.out.println(blockData + " (Key " + access.getKeyValue().getKey().name() + ": " + HexUtils.bytesToHexString(key) + ")");
                }
            }
            return written;
        }
        return false;
    }

    /**
     * Reads a Mifare Classic 1K block.
     * @param readerWriter the reader
     * @param access the access
     * @return a string representation of the block data, null if the block can't be read
     */
    private static String readMifareClassic1KBlock(MfClassicReaderWriter readerWriter, MfClassicAccess access)
            throws CardException {
        String data = null;
        try {
            MfBlock block = readerWriter.readBlock(access)[0];
            data = bytesToHexString(block.getData());
        } catch (IOException ioe) {
            if (ioe.getCause() instanceof CardException) {
                throw (CardException) ioe.getCause();
            }
        }
        return data;
    }

    /**
     * Writes a Mifare Classic 1K block.
     * @param readerWriter the reader
     * @param access the access
     * @param block the block to be written
     * @return true if the block has been written, false otherwise
     */
    private static boolean writeMifareClassic1KBlock(MfClassicReaderWriter readerWriter, MfClassicAccess access, MfBlock block) throws CardException {
        boolean written = false;
        try {
            readerWriter.writeBlock(access, block);
            written = true;
        } catch (IOException ioe) {
            if (ioe.getCause() instanceof CardException) {
                throw (CardException) ioe.getCause();
            }
        }
        return written;
    }

    /**
     * Dumps Mifare Classic 1K block data.
     * @param readerWriter the reader
     * @param sectorId the sector to be read
     * @param blockId the block to be read
     * @param key the key to be user for reading
     */
    public static String readMifareClassic1KBlock(MfClassicReaderWriter readerWriter, int sectorId, int blockId, byte[] key) throws CardException {
        System.out.printf("Sector %02d block %02d: ", sectorId, blockId);

        // For each provided key...
        if (isValidMifareClassic1KKey(key)) {
            // Reading with key A
            MemoryLayout memoryLayout = readerWriter.getMemoryLayout();
            MfClassicAccess access = new MfClassicAccess(new KeyValue(Key.A, key), sectorId, blockId, memoryLayout.getBlocksPerSector(sectorId));
            String blockData = readMifareClassic1KBlock(readerWriter, access);
            if (blockData == null) {
                // Reading with key B
                access = new MfClassicAccess(new KeyValue(Key.B, key), sectorId, blockId, memoryLayout.getBlocksPerSector(sectorId));
                blockData = readMifareClassic1KBlock(readerWriter, access);
            }
            if (blockData != null) {
                // Block read
                System.out.println(blockData + " (Key " + access.getKeyValue().getKey().name() + ": " + HexUtils.bytesToHexString(key) + ")");
                return blockData;
            }
        }
        // All keys tested, failed to read block
        System.out.println("<Failed to read block>");
        return null;
    }
}